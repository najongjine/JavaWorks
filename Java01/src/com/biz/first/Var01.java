package com.biz.first;

public class Var01 {

	public static void main(String[] args) {
		int num1=30;
		int num2=40;
		
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num2-num1);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		System.out.println(num2/num1);

	}

}
