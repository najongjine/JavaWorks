package com.biz.first;

public class Var02 {

	public static void main(String[] args) {
		int intNum1=30; 
		int intNum2=40;
		
		System.out.println(intNum1+intNum2);
		System.out.println(intNum1-intNum2);
		System.out.println(intNum2-intNum1);
		System.out.println(intNum1*intNum2);
		System.out.println(intNum1/intNum2);
		System.out.println(intNum2/intNum1);
		

	}

}
