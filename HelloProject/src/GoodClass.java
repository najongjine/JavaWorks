
public class GoodClass {
	//System.out.println("Good Morning");
	public static void main(String[] args) {
		//println 은 괄호한에 표현된 어떤 것들을 console에 보여라 하는 명령문
		System.out.println("Good Morning");
		System.out.println();
		
		//숫자 데이터 || 숫자 리터럴 이라고 부른다
		//4칙 연산을 수행할수 있다
		System.out.println(30*40);
		System.out.println(40/20);
		System.out.println(30-20);
		System.out.println(40+20);
		
		//double quotation 으로 묶여있는 값은 문자영 데이터 || 문자영 리터라 부름
		//문자열 데이터는 주로 덧셈 연산만 수행할수 있고 문자열들을 서로 연결 시키는 역활만 수행한다
		System.out.println("대한"+"민국");
		
		System.out.println(30+"40");

	}
}
